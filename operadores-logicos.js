const a = false, b = false;

if (a || b){
    console.log('TRUE');
}else{
    console.log('FALSE');
}

if (a && b){
    console.log('TRUE');
}else {
    console.log('FALSE');
}

// ||   -> OU; (uma das variaveis terá que ser verdadeira);
// &&   -> E; (ambas variaveis deverão ser verdadeiras);
// !    -> NÃO;